#!/usr/bin/env python3
"""Benchmark the self-hosted compiler across island-depth stages, emitting a LaTeX table.

A stage is the specializer ``COMPILE_SPECIALIZED`` at one island depth bound (see ``_specialize``): a
larger bound admits more and larger call-by-value islands, so more of the compiler is compiled as
strict native code and less is left to the interpreter. For each stage this regenerates the
self-compiled compiler (``COMPILE`` specialized at that bound) in a FRESH subprocess, so the peak
resident memory is that stage's alone, and records the islands spliced, the largest maximal island
admitted, the wall-clock time, and the peak memory.

The result is committed as ``generated/self-compilation-benchmark.tex`` and ``\\input`` by the paper.
The timing and memory columns are a measured snapshot (machine dependent); the island counts and sizes
are deterministic. Run from the repo root with the package on the path:

    PYTHONPATH=packages/first-order-lambda/src:packages/fixpoints/src \\
        python3 first-order/benchmark_self_compilation.py
"""

from __future__ import annotations

import resource
import subprocess
import sys
import time

from pathlib import Path

_REPO_ROOT = Path(__file__).resolve().parent.parent
_OUTPUT = _REPO_ROOT / "first-order" / "generated" / "self-compilation-benchmark.tex"

# The stages: island depth bounds. 8 is the small committed default; 128 is the large committed
# artifact (it admits the 131/135-node islands while excluding the two depth-188/191 monster
# combinators whose simple typing would drive a single pass to ~12 GB).
_BOUNDS = (8, 64, 128)


def _worker(bound: int) -> None:
    """Generate the self-compiled compiler at one island depth bound; print the stage's metrics."""
    from first_order_lambda._compiler import COMPILE
    from first_order_lambda._dsl import build
    from first_order_lambda._prelude import church
    from first_order_lambda._specialize import compile_specialized_lambda

    start = time.perf_counter()
    source = compile_specialized_lambda(build(COMPILE), island_depth=church(bound))
    elapsed = time.perf_counter() - start
    peak_gb = resource.getrusage(resource.RUSAGE_SELF).ru_maxrss / 1e6
    print(f"{source.count('value_island(')} {elapsed:.6f} {peak_gb:.6f} {len(source)}")


def _row(bound: int, islands: int, seconds: float, peak_gb: float, chars: int) -> str:
    return f"  {bound} & {islands} & {seconds:.1f} & {peak_gb:.2f} & {chars / 1000:.1f} \\\\"


def main() -> None:
    rows: "list[str]" = []
    for bound in _BOUNDS:
        completed = subprocess.run(
            [sys.executable, str(Path(__file__).resolve()), "--worker", str(bound)],
            check=True,
            capture_output=True,
            text=True,
        )
        islands, seconds, peak_gb, chars = completed.stdout.split()
        rows.append(_row(bound, int(islands), float(seconds), float(peak_gb), int(chars)))
    table = "\n".join([
        "% Generated by first-order/benchmark_self_compilation.py. Do not edit.",
        "\\begin{tabular}{rrrrr}",
        "\\hline",
        "Island depth & Islands & Time (s) & Peak memory (GB) & Artifact (KB) \\\\",
        "\\hline",
        *rows,
        "\\hline",
        "\\end{tabular}",
        "",
    ])
    _OUTPUT.write_text(table)
    print(f"wrote {_OUTPUT}")


if __name__ == "__main__":
    if len(sys.argv) == 3 and sys.argv[1] == "--worker":
        _worker(int(sys.argv[2]))
    else:
        main()
