"""Build the generated artifacts: the LaTeX appendix fragment and the self-compiled compiler module.

These are pure, deterministic string builders. ``first-order/generate_examples.py`` writes them to
disk, and tests assert the committed files match these builders, so the artifacts cannot drift from
the compiler. Every example's Python is the real ``compile_to_source`` output; the displayed lambda is
``term_to_latex`` of the term (readable names) for the small terms, or a named-combinator form (as the
paper writes it) for the larger composite terms, whose full expansion would be unreadable.
"""

from __future__ import annotations

from first_order_lambda._compiler import COMPILE, Runtime, compile_to_source
from first_order_lambda._dsl import Builder, build
from first_order_lambda._latex import term_to_latex
from first_order_lambda._prelude import (
    IDENTITY,
    IS_ZERO,
    KESTREL,
    MULT,
    PLUS,
    SUCC,
    church,
)

_LATEX_HEADER = (
    "% Generated by first-order/generate_examples.py from the first_order_lambda compiler.\n"
    "% Do not edit; regenerate with: python3 first-order/generate_examples.py\n"
)

_PYTHON_HEADER = (
    "# Generated by first-order/generate_examples.py from the COMPILE lambda term. Do not edit.\n"
    "# This is the pure-lambda compiler COMPILE compiled to Python by the bootstrap compiler\n"
    "# (compile_to_source(build(COMPILE))): a Python function from a host-encoded quoted source term\n"
    "# to a host-encoded quoted Python expression (see _compiler.compile_with for the calling\n"
    "# convention). Regenerate with: python3 first-order/generate_examples.py\n"
)

# (title, builder, runtime, latex): latex is None to auto-render the term with readable names, or a
# named-combinator string for the composite terms whose full expansion would be unreadable. The
# call-by-value target is strict; the call-by-name target emits the lambda term's force/Thunk
# wrapping. The cyclic and divergent examples belong to the interpret target, which is reintroduced
# once that target's runtime lands.

# Readable runtime labels for the example headings (the enum name has underscores).
_RUNTIME_LABEL = {
    Runtime.CALL_BY_VALUE: "call-by-value",
    Runtime.CALL_BY_NAME: "call-by-name",
    Runtime.CALL_BY_NEED: "call-by-need",
    Runtime.INTERPRET: "interpret",
}
_EXAMPLES: tuple[tuple[str, Builder, Runtime, str | None], ...] = (
    ("Identity", IDENTITY, Runtime.CALL_BY_VALUE, None),
    ("The constant combinator $K$", KESTREL, Runtime.CALL_BY_VALUE, None),
    ("Church numeral $2$", church(2), Runtime.CALL_BY_VALUE, None),
    ("Successor", SUCC, Runtime.CALL_BY_VALUE, None),
    ("Addition", PLUS, Runtime.CALL_BY_VALUE, None),
    ("Multiplication", MULT, Runtime.CALL_BY_VALUE, None),
    ("Zero test", IS_ZERO, Runtime.CALL_BY_VALUE, None),
    ("Identity, call-by-name target", IDENTITY, Runtime.CALL_BY_NAME, None),
)


def _listing(source: str) -> str:
    return "\\begin{lstlisting}\n" + source + "\n\\end{lstlisting}\n"


def _example_block(title: str, builder: Builder, runtime: Runtime, latex: str | None) -> str:
    node = build(builder)
    rendered = latex if latex is not None else term_to_latex(node)
    note = "" if runtime is Runtime.CALL_BY_VALUE else f"~\\textnormal{{({_RUNTIME_LABEL[runtime]} runtime)}}"
    heading = f"\\medskip\\noindent\\textbf{{{title}.}}{note}\\quad ${rendered}$\n"
    return heading + _listing(compile_to_source(node, runtime))


def compiler_examples_fragment() -> str:
    """The LaTeX fragment: a comparison of each source lambda and the Python the compiler emits."""
    blocks = [_example_block(title, builder, runtime, latex) for title, builder, runtime, latex in _EXAMPLES]
    bootstrap = (
        "\\medskip\\noindent\\textbf{The compiler itself, compiled to Python (self-hosting).}\\quad "
        "The compiler $\\mathtt{compile}$ above, compiled by itself, is the Python function committed "
        "as the generated module \\texttt{\\_generated\\_compiler.py}:\n"
        + _listing(compile_to_source(build(COMPILE)))
    )
    return _LATEX_HEADER + "\n" + "\n".join(blocks) + "\n" + bootstrap


def generated_compiler_module_text() -> str:
    """The Python source of the self-compiled compiler: COMPILE compiled to a Python function."""
    return _PYTHON_HEADER + "\ncompiled_compiler = " + compile_to_source(build(COMPILE)) + "\n"
